SELECT CALENDAR_MONTH.MONTHDATE AS MONTHDATE,
  GROUP_TRAINER.GROUP_ID,
  GROUP_TRAINER.TRAINER_ID,
  GROUP_TRAINER.SALARY_TYPE_ID,
  GROUP_TRAINER.PERCENT
FROM CALENDAR_MONTH INNER JOIN GROUP_TRAINER
ON DATESERIAL(GROUP_TRAINER.FROM_YEAR, GROUP_TRAINER.FROM_MONTH, 1) <= CALENDAR_MONTH.MONTHDATE
AND CALENDAR_MONTH.MONTHDATE <= DATESERIAL(IIf(GROUP_TRAINER.TO_YEAR IS NULL, 2099, GROUP_TRAINER.TO_YEAR), IIf(GROUP_TRAINER.TO_MONTH IS NULL, 12, GROUP_TRAINER.TO_MONTH),1)

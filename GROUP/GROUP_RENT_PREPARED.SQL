SELECT CALENDAR_MONTH.MONTHDATE,
  GROUP_RENT.GROUP_ID,
  GROUP_RENT.RENT_TYPE_ID,
  GROUP_RENT.AMOUNT
FROM CALENDAR_MONTH INNER JOIN GROUP_RENT
ON 
    DATESERIAL(GROUP_RENT.FROM_YEAR, GROUP_RENT.FROM_MONTH, 1) <= CALENDAR_MONTH.MONTHDATE
    AND CALENDAR_MONTH.MONTHDATE <= DATESERIAL(IIf(GROUP_RENT.TO_YEAR IS NULL, 2099, GROUP_RENT.TO_YEAR), IIf(GROUP_RENT.TO_MONTH IS NULL, 12, GROUP_RENT.TO_MONTH),1)

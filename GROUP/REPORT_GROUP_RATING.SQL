SELECT GROUP_STATS.COMPANY,
GROUP_STATS.FOR_YEAR,
GROUP_STATS.FOR_MONTH,
ORGANIZATION.FULLNAME AS COMPANY_NAME,
HALL.FULLNAME AS HALL_NAME,
GROUPS.FULLNAME AS GROUP_NAME,
SPORT.FULLNAME AS SPORT_NAME,
PERSON.LASTNAME AS TRAINER_SURNAME,
GROUP_TYPE.FULLNAME AS GROUP_TYPE_NAME,
GROUP_STATS.GROUP_SIZE,
GROUP_STATS.GROUP_PAYMENT
FROM (PERSON INNER JOIN TRAINER ON PERSON.PERSON_ID = TRAINER.PERSON_ID)
INNER JOIN (SPORT
  INNER JOIN (GROUP_TYPE
  INNER JOIN (((GROUP_STATS
    INNER JOIN GROUPS ON GROUP_STATS.GROUP_ID = GROUPS.GROUP_ID)
    INNER JOIN ORGANIZATION ON GROUPS.ORGANIZATION_ID = ORGANIZATION.ORGANIZATION_ID)
    INNER JOIN HALL ON GROUPS.HALL_ID = HALL.HALL_ID)
  ON GROUP_TYPE.GROUP_TYPE_ID = GROUPS.GROUP_TYPE_ID)
ON SPORT.SPORT_ID = GROUPS.SPORT_ID)
ON TRAINER.TRAINER_ID = GROUPS.TRAINER_ID;

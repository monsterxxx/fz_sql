SELECT CALENDAR_MONTH.CALENDAR_MONTH,
  GROUP_PLAN.GROUP_ID,
  GROUP_PLAN.AMOUNT
FROM CALENDAR_MONTH INNER JOIN GROUP_PLAN
ON DATESERIAL(GROUP_PLAN.FROM_YEAR, GROUP_PLAN.FROM_MONTH, 1) <= CALENDAR_MONTH.CALENDAR_MONTH
AND CALENDAR_MONTH.CALENDAR_MONTH <= DATESERIAL(IIf(GROUP_PLAN.TO_YEAR IS NULL, 2099, GROUP_PLAN.TO_YEAR), IIf(GROUP_PLAN.TO_MONTH IS NULL, 12, GROUP_PLAN.TO_MONTH),1)

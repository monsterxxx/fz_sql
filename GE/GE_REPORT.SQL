SELECT GE_UNITED.COLUMN AS Графа,
  GE_UNITED.COLUMN AS Для_формата,
  GE_UNITED.MONTH AS Месяц,
  GE_UNITED.GROUP_ID,
  GE_UNITED.AMOUNT AS Сумма,
  ORGANIZATION.FULLNAME AS Компания,
  HALL.FULLNAME AS Зал,
  GROUPS.FULLNAME AS Группа,
  [LASTNAME]+" "+Left([FIRSTNAME],1)+". "+Left([PATRONYMIC],1)+"." AS Тренер,
  SPORT.FULLNAME AS Спорт,
  GROUPS.MAX_SIZE AS Вместимость
FROM
(PERSON INNER JOIN TRAINER ON PERSON.PERSON_ID = TRAINER.PERSON_ID)
  INNER JOIN
    (SPORT INNER JOIN
      (
        (
          (GE_UNITED INNER JOIN GROUPS ON GE_UNITED.GROUP_ID = GROUPS.GROUP_ID)
        INNER JOIN ORGANIZATION ON GROUPS.ORGANIZATION_ID = ORGANIZATION.ORGANIZATION_ID)
      INNER JOIN HALL ON GROUPS.HALL_ID = HALL.HALL_ID)
    ON SPORT.SPORT_ID = GROUPS.SPORT_ID)
  ON TRAINER.TRAINER_ID = GROUPS.TRAINER_ID
WHERE (((GROUPS.HIDDEN)=False) AND ((GROUPS.POTENTIAL)=False));

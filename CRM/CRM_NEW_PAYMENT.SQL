SELECT 'Новые оплатили' AS COLUMN,
PAYMENT.COMPANY,
PAYMENT.PERSON_ID,
DATESERIAL(PAYMENT.FOR_YEAR, PAYMENT.FOR_MONTH, 1) AS PAYMONTH
FROM (CRM_NEW_PAYMENT_PRE INNER JOIN PAYMENT ON PAYMENT.PAYMENT_ID = CRM_NEW_PAYMENT_PRE.PAYMENT_ID) INNER JOIN PERSON ON CRM_NEW_PAYMENT_PRE.PERSON_ID = PERSON.PERSON_ID
WHERE PERSON.INFOSOURCE <> 'Старый клиент'

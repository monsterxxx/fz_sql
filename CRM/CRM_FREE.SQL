SELECT 'Всего занималось' AS COLUMN,
  'Занимались бесплатно' AS COLUMN1,
  CRM_FREE_PRE.ORGANIZATION_ID,
  CRM_FREE_PRE.PERSON_ID, 
  CRM_FREE_PRE.FREEMONTH
FROM (CRM_FREE_PRE LEFT JOIN CRM_PAYMENT ON (CRM_FREE_PRE.FREEMONTH = CRM_PAYMENT.PAYMONTH) AND (CRM_FREE_PRE.PERSON_ID = CRM_PAYMENT.PERSON_ID)) LEFT JOIN CRM_DEBT ON (CRM_FREE_PRE.FREEMONTH = CRM_DEBT.DEBTMONTH) AND (CRM_FREE_PRE.PERSON_ID = CRM_DEBT.PERSON_ID)
WHERE (((CRM_PAYMENT.PERSON_ID) Is Null) AND ((CRM_DEBT.PERSON_ID) Is Null));

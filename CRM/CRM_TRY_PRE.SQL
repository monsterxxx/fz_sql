SELECT GROUPS.ORGANIZATION_ID,
  CLIENT.PERSON_ID,
  DATESERIAL(CHARGE.FOR_YEAR, CHARGE.FOR_MONTH, 1) AS TRYMONTH
FROM GROUPS INNER JOIN (CLIENT INNER JOIN CHARGE ON CLIENT.CLIENT_ID = CHARGE.CLIENT_ID) ON GROUPS.GROUP_ID = CHARGE.GROUP_ID
WHERE (((CHARGE.CHARGE_TYPE_NAME)="Разовые") AND ((CHARGE.DISCOUNT_PERCENT1)=1));

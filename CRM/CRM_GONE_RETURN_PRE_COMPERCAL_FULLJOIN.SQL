SELECT COMPANY,
  PERSON.PERSON_ID,
  CALENDAR_MONTH_DISTINCT.CALENDAR_MONTH AS CALMONTH
FROM (SELECT DISTINCT CALENDAR.CALENDAR_MONTH AS CALENDAR_MONTH FROM CALENDAR) AS CALENDAR_MONTH_DISTINCT, PERSON, (SELECT TOP 1 1 AS COMPANY FROM CALENDAR UNION ALL SELECT TOP 1 76 FROM CALENDAR)
WHERE PERSON.POTENTIAL = FALSE
ORDER BY COMPANY,
  PERSON.PERSON_ID,
  CALENDAR_MONTH_DISTINCT.CALENDAR_MONTH

SELECT DISTINCT 'Всего занималось' AS COLUMN,
  GROUPS.ORGANIZATION_ID,
  CLIENT.PERSON_ID,
  DATESERIAL(YEAR(TRAINING.CREATE_DATE), MONTH(TRAINING.CREATE_DATE), 1) AS ATTMONTH
FROM GROUPS INNER JOIN (TRAINING INNER JOIN (CLIENT INNER JOIN ATTENDANCE ON CLIENT.CLIENT_ID = ATTENDANCE.CLIENT_ID) ON TRAINING.TRAINING_ID = ATTENDANCE.TRAINING_ID) ON GROUPS.GROUP_ID = TRAINING.GROUP_ID
WHERE CAME = TRUE

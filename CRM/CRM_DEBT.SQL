SELECT DISTINCT 'Всего занималось' AS COLUMN,
  'Должники' AS COLUMN1,
  GROUPS.ORGANIZATION_ID,
  CLIENT.PERSON_ID,
  DATESERIAL(CLIENT_MONTH_GROUP_DEBT.FOR_YEAR, CLIENT_MONTH_GROUP_DEBT.FOR_MONTH, 1) AS DEBTMONTH
FROM (CLIENT_MONTH_GROUP_DEBT INNER JOIN CLIENT ON CLIENT.CLIENT_ID = CLIENT_MONTH_GROUP_DEBT.CLIENT_ID) INNER JOIN GROUPS ON GROUPS.GROUP_ID = CLIENT_MONTH_GROUP_DEBT.GROUP_ID
WHERE CLIENT_MONTH_GROUP_DEBT.DEBT>0

SELECT CLIENT_TOTAL_GROUP_DEBT.DEBT,
       CLIENT_TOTAL_GROUP_DEBT.CLIENT_ID,
       CLIENT_TOTAL_GROUP_DEBT.GROUP_ID,
       T1.[Count-CAME]
FROM
  (CLIENT_TOTAL_GROUP_DEBT INNER JOIN GROUPS ON CLIENT_TOTAL_GROUP_DEBT.GROUP_ID = GROUPS.GROUP_ID)
  LEFT JOIN
    (SELECT * FROM CLIENT_MONTH_GROUP_ATTENDANCE
     WHERE (CLIENT_MONTH_GROUP_ATTENDANCE.FOR_MONTH)=Month(Date()) AND (CLIENT_MONTH_GROUP_ATTENDANCE.FOR_YEAR)=Year(Date()))  AS T1
  ON (CLIENT_TOTAL_GROUP_DEBT.GROUP_ID = T1.GROUP_ID) AND (CLIENT_TOTAL_GROUP_DEBT.CLIENT_ID = T1.CLIENT_ID)
ORDER BY GROUPS.FULLNAME;

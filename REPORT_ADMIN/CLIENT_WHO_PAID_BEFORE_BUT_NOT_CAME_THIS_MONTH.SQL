SELECT [LASTNAME] & " " & [FIRSTNAME] AS [Клиент],
CLIENT_COUNT_CAME_THIS_MONTH.PERSON_ID,
ORGANIZATION.FULLNAME AS [Компания],
CLIENT_COUNT_CAME_THIS_MONTH.ORGANIZATION_ID,
CLIENT_PAYMONTH_FIRST.FIRST_PAYMONTH AS [Первая оплата],
CLIENT_PAYMONTH_LAST.LAST_PAYMONTH AS [Последняя оплата],
CLIENT_PAYMONTH_DISTINCT_COUNT.COUNT_DISTINCT_PAYMONTH AS [Кол-во оплаченных месяцев],
PERSON.REMIND_DATE AS [Напоминание],
PERSON.REFUSAL_REASON AS [Причина отказа],
PERSON.HAS_PASS AS [Пропуск FZ постоянный]
FROM ((((CLIENT_COUNT_CAME_THIS_MONTH INNER JOIN CLIENT_PAYMONTH_FIRST ON CLIENT_COUNT_CAME_THIS_MONTH.PERSON_ID = CLIENT_PAYMONTH_FIRST.PERSON_ID) INNER JOIN CLIENT_PAYMONTH_LAST ON CLIENT_PAYMONTH_FIRST.PERSON_ID = CLIENT_PAYMONTH_LAST.PERSON_ID) INNER JOIN CLIENT_PAYMONTH_DISTINCT_COUNT ON CLIENT_PAYMONTH_LAST.PERSON_ID = CLIENT_PAYMONTH_DISTINCT_COUNT.PERSON_ID) INNER JOIN PERSON ON CLIENT_COUNT_CAME_THIS_MONTH.PERSON_ID = PERSON.PERSON_ID) INNER JOIN ORGANIZATION ON CLIENT_COUNT_CAME_THIS_MONTH.ORGANIZATION_ID = ORGANIZATION.ORGANIZATION_ID
WHERE (((CLIENT_COUNT_CAME_THIS_MONTH.COUNT_CAME_THIS_MONTH)=0));

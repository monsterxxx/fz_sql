SELECT BUDGET_DIVIDEND.COMPANY,
  BUDGET_DIVIDEND.PERSON_ID,
  BUDGET_DIVIDEND.BUDMONTH,
  BUDGET_DIVIDEND.DIV,
  BUDGET_DIVIDEND_PAID.SUMMA,
  [DIV]-IIf([SUMMA] Is Null,0,[SUMMA]) AS DIV_BALANCE
FROM BUDGET_DIVIDEND LEFT JOIN BUDGET_DIVIDEND_PAID ON (BUDGET_DIVIDEND.BUDMONTH = BUDGET_DIVIDEND_PAID.BUDMONTH)
  AND (BUDGET_DIVIDEND.PERSON_ID = BUDGET_DIVIDEND_PAID.PERSON_ID)
  AND (BUDGET_DIVIDEND.COMPANY = BUDGET_DIVIDEND_PAID.COMPANY);

SELECT BUDGET_PAYMONTH_SUM.*,
  BUDGET_PAYMONTH_SUM.PROFIT_MONTH
  - IIF(TRAINER_SALARY_DEBT_SUM.TRAINER_DEBT_MONTH IS NULL, 0, TRAINER_SALARY_DEBT_SUM.TRAINER_DEBT_MONTH)
  - IIF(EMPLOYEE_SALARY_DEBT_SUM.EMPLOYEE_DEBT_MONTH IS NULL, 0, EMPLOYEE_SALARY_DEBT_SUM.EMPLOYEE_DEBT_MONTH) AS CLEAR_PROFIT_MONTH
FROM (BUDGET_PAYMONTH_SUM
  LEFT JOIN EMPLOYEE_SALARY_DEBT_SUM ON (BUDGET_PAYMONTH_SUM.BUDMONTH = EMPLOYEE_SALARY_DEBT_SUM.MONTHDATE) AND (BUDGET_PAYMONTH_SUM.COMPANY = EMPLOYEE_SALARY_DEBT_SUM.COMPANY))
  LEFT JOIN TRAINER_SALARY_DEBT_SUM ON (BUDGET_PAYMONTH_SUM.BUDMONTH = TRAINER_SALARY_DEBT_SUM.MONTHDATE) AND (BUDGET_PAYMONTH_SUM.COMPANY = TRAINER_SALARY_DEBT_SUM.COMPANY);

SELECT BUDGET_MONTH_PRE.*,
  IIf(BUDGET_DIVIDEND_SUM.DIV_MONTH IS NULL, 0, BUDGET_DIVIDEND_SUM.DIV_MONTH) AS DIV_MONTH,
  BUDGET_MONTH_PRE.CLEAR_PROFIT_MONTH
  - IIf(BUDGET_DIVIDEND_SUM.DIV_MONTH IS NULL, 0, BUDGET_DIVIDEND_SUM.DIV_MONTH)
  - BUDGET_MONTH_PRE.INVESTMENT_MONTH
  - BUDGET_MONTH_PRE.LEND_MONTH
  - BUDGET_MONTH_PRE.BANK_EXPENSE_MONTH
  + IIf(BUDGET_MONTH_PRE.BUDMONTH <> DATESERIAL(2014, 7, 1), 0, SWITCH(BUDGET_MONTH_PRE.COMPANY = 1, -161350, BUDGET_MONTH_PRE.COMPANY = 76, 182620, BUDGET_MONTH_PRE.COMPANY = 117, -29807, BUDGET_MONTH_PRE.COMPANY = 119, 40648)) AS BANK_MONTH
FROM BUDGET_MONTH_PRE
  LEFT JOIN BUDGET_DIVIDEND_SUM ON (BUDGET_MONTH_PRE.BUDMONTH = BUDGET_DIVIDEND_SUM.BUDMONTH)
  AND (BUDGET_MONTH_PRE.COMPANY = BUDGET_DIVIDEND_SUM.COMPANY)

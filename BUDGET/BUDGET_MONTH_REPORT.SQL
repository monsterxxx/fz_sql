SELECT ORGANIZATION.FULLNAME AS Компания,
  IIf(COMPANY_TO IS NOT NULL, ORGANIZATION_1.FULLNAME, IIf(PERSON_TO IS NOT NULL, PERSON.LASTNAME & " " & LEFT(PERSON.FIRSTNAME, 1) & "." & LEFT(PERSON.PATRONYMIC, 1) & "." )) AS Кому,
  COLUMN AS Графа,
  BUDMONTH AS Месяц,
  SUMMA AS Сумма
FROM ((BUDGET_MONTH_REPORT_UNITED LEFT JOIN PERSON ON BUDGET_MONTH_REPORT_UNITED.PERSON_TO = PERSON.PERSON_ID)
LEFT JOIN ORGANIZATION AS ORGANIZATION_1 ON BUDGET_MONTH_REPORT_UNITED.COMPANY_TO = ORGANIZATION_1.ORGANIZATION_ID)
INNER JOIN ORGANIZATION ON BUDGET_MONTH_REPORT_UNITED.COMPANY = ORGANIZATION.ORGANIZATION_ID;

SELECT COMPANY,
  COLUMN,
  BUDMONTH,
  SUMMA
FROM (
  SELECT COMPANY, BUDMONTH, 'Касса' AS COLUMN, KASSA_MONTH AS SUMMA, NULL AS COMPANY_TO, NULL AS PERSON_TO FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Счет', ACCOUNT_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Сейф', SAFE_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Прибыль', PROFIT_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Чистая прибыль', CLEAR_PROFIT_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Инвестиции', INVESTMENT_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Баланс Выемка/Внос', BALANCE_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Банк', BANK_MONTH, NULL, NULL FROM BUDGET_MONTH UNION ALL
  SELECT COMPANY, BUDMONTH, 'Расходы банка', BANK_EXPENSE_MONTH, NULL, NULL FROM BUDGET_MONTH
) AS BUDGET_MONTH_VERTICAL

SELECT * FROM (
  SELECT PAYMENT.COMPANY AS COMPANY,
    PAYMENT.ORGANIZATION_ID AS COMPANY_TO,
    PAYMENT.PERSON_ID AS PERSON_TO,
    IIf(PAYMENT.PAYMENT_TYPE_ID = 6, 'Выплачено дивидендов', 'Займы Выдано/Получено') AS COLUMN,
    DATESERIAL(YEAR(PAYMENT.PAYMENT_DATE), MONTH(PAYMENT.PAYMENT_DATE), 1) AS BUDMONTH,
    PAYMENT.SUMMA AS SUMMA
  FROM PAYMENT
  WHERE (PAYMENT.PAYMENT_TYPE_ID IN (6,28))
  UNION ALL
  SELECT COMPANY,
    NULL AS COMPANY_TO,
    PERSON_ID AS PERSON_TO,
    'Дивиденды' AS COLUMN,
    BUDMONTH,
    DIV AS SUMMA
  FROM BUDGET_DIVIDEND
  UNION ALL
  SELECT BUDGET_MONTH_REPORT_MAIN.COMPANY AS COMPANY,
    NULL AS COMPANY_TO,
    NULL AS PERSON_TO,
    BUDGET_MONTH_REPORT_MAIN.COLUMN AS COLUMN,
    BUDGET_MONTH_REPORT_MAIN.BUDMONTH AS BUDMONTH,
    CCUR(BUDGET_MONTH_REPORT_MAIN.SUMMA) AS SUMMA
  FROM BUDGET_MONTH_REPORT_MAIN
  UNION ALL
  SELECT COMPANY,
    NULL,
    PERSON_ID,
    'Баланс дивидендов',
    BUDMONTH,
    DIV_BALANCE
  FROM BUDGET_DIVIDEND_BALANCE
  UNION ALL
  SELECT COMPANY,
    NULL,
    PERSON_ID,
    'Невыплаченная ЗП инструкторам',
    MONTHDATE,
    DEBT
  FROM TRAINER_SALARY_DEBT
  UNION ALL
  SELECT COMPANY,
    NULL,
    PERSON_ID,
    'Невыплаченная ЗП работникам',
    MONTHDATE,
    DEBT
  FROM EMPLOYEE_SALARY_DEBT
) AS T1

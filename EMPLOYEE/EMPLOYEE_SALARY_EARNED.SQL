SELECT DISTINCT CALENDAR.CALENDAR_MONTH AS MONTHDATE,
  EMPLOYEE.ORGANIZATION_ID AS COMPANY,
  EMPLOYEE_SALARY.EMPLOYEE_ID,
  EMPLOYEE.PERSON_ID,
  EMPLOYEE_SALARY.SALARY AS SUMM_EARNED
FROM CALENDAR INNER JOIN
  (EMPLOYEE_SALARY INNER JOIN EMPLOYEE ON EMPLOYEE_SALARY.EMPLOYEE_ID = EMPLOYEE.EMPLOYEE_ID)
  ON  ((EMPLOYEE_SALARY.FROM_MONTHDATE <=CALENDAR.CALENDAR_MONTH)
      AND ((CALENDAR.CALENDAR_MONTH<=EMPLOYEE_SALARY.TO_MONTHDATE) OR (EMPLOYEE_SALARY.TO_MONTHDATE IS NULL)));

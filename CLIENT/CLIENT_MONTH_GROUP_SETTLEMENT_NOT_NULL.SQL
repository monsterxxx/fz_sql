SELECT CLIENT_MONTH_GROUP_SETTLEMENT.CLIENT_ID,
CLIENT_MONTH_GROUP_SETTLEMENT.GROUP_ID,
CLIENT_MONTH_GROUP_SETTLEMENT.FOR_MONTH,
CLIENT_MONTH_GROUP_SETTLEMENT.DIRECTION,
CLIENT_MONTH_GROUP_SETTLEMENT.PRICE,
CLIENT_MONTH_GROUP_SETTLEMENT.CHARGE_TYPE_NAME,
CLIENT_MONTH_GROUP_SETTLEMENT.Expr1006,
CLIENT_MONTH_GROUP_SETTLEMENT.DISCOUNT_NAME1,
CLIENT_MONTH_GROUP_SETTLEMENT.Expr1008,
CLIENT_MONTH_GROUP_SETTLEMENT.DISCOUNT_NAME2,
CLIENT_MONTH_GROUP_SETTLEMENT.Expr1010,
CLIENT_MONTH_GROUP_SETTLEMENT.CHARGE_DATE,
CLIENT_MONTH_GROUP_SETTLEMENT.SUMMA,
TRAINING.CREATE_DATE, *
FROM (CLIENT_MONTH_GROUP_SETTLEMENT
  INNER JOIN CLIENT_MONTH_GROUP_DEBT ON (CLIENT_MONTH_GROUP_SETTLEMENT.FOR_MONTH = CLIENT_MONTH_GROUP_DEBT.FOR_MONTH)
    AND (CLIENT_MONTH_GROUP_SETTLEMENT.GROUP_ID = CLIENT_MONTH_GROUP_DEBT.GROUP_ID)
    AND (CLIENT_MONTH_GROUP_SETTLEMENT.CLIENT_ID = CLIENT_MONTH_GROUP_DEBT.CLIENT_ID))
  LEFT JOIN TRAINING ON CLIENT_MONTH_GROUP_SETTLEMENT.TRAINING_ID = TRAINING.TRAINING_ID
WHERE (((CLIENT_MONTH_GROUP_DEBT.FOR_MONTH)>=Month(Date())-1));

SELECT ORGANIZATION.FULLNAME AS [Компания],
  CLIENT_PAYMONTH_LAST.PERSON_ID,
  CLIENT_PAYMONTH_LAST.LAST_PAYMONTH AS [Последний оплаченый месяц],
  PERSON.LASTNAME & " " & PERSON.FIRSTNAME AS [Фамилия Имя],
  DateDiff("yyyy", PERSON.BIRTHDAY, Date()) AS [Возраст]
FROM (CLIENT_PAYMONTH_LAST INNER JOIN PERSON ON PERSON.PERSON_ID = CLIENT_PAYMONTH_LAST.PERSON_ID) INNER JOIN ORGANIZATION ON ORGANIZATION.ORGANIZATION_ID = CLIENT_PAYMONTH_LAST.COMPANY
